import numpy

#finding sinh of a radian number
sinhNum = numpy.sinh(numpy.pi/3)

print(sinhNum)

#converting sinh to radians
sinhRad = numpy.arcsinh(sinhNum)

print(sinhRad)

#finding cosh of radians
coshNum = numpy.cosh(numpy.pi/3)

print(coshNum)

#converting cosh to radians
coshRad = numpy.arccosh(coshNum)

print(coshRad)